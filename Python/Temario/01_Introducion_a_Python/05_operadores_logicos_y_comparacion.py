# Tema 1 Comparación y operadores lógicos

x = 5
y = 10

# Comparación
print("Mayor:", x > y)
print("Menor:", x < y)
print("Igual:", x == y)
print("Distinto:", x != y)

# Lógicos
llueve = True
tengo_paraguas = False

print("AND:", llueve and tengo_paraguas)
print("OR:", llueve or tengo_paraguas)
print("NOT:", not llueve)


if __name__ == "__main__":
    print("Comparaciones y operadores lógicos funcionando correctamente.")
