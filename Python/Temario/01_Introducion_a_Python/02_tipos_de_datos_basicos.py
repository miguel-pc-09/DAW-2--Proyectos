# Tema 1 Tipos de datos básicos en Python
# Aquí meto enteros, floats, booleanos y strings.

# Entero
numero_entero = 25

# Float (decimal)
numero_decimal = 3.14

# Cadena de texto
nombre = "Miguel"

# Booleano
es_mayor = True

print(numero_entero, type(numero_entero))
print(numero_decimal, type(numero_decimal))
print(nombre, type(nombre))
print(es_mayor, type(es_mayor))



